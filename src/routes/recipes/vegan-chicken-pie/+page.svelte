			<h2 class="subheading">Ingredients</h2>
			<ul class="body">
				<li>2 tablespoons olive oil</li>
				<li>1 small onion, chopped</li>
				<li>3 garlic cloves, minced</li>
				<li>⅓ cup all-purpose flour</li>
				<li>2 cups vegetable broth</li>
				<li>1 cup unsweetened almond milk</li>
				<li>3 cups frozen mixed vegetables (carrots, corn, green beans, peas)</li>
				<li>1 large russet potato, chopped into small chunks</li>
				<li>8 ounces vegan "chicken" or baked tofu, cut into chunks (optional)</li>
				<li>½ teaspoon salt, or to taste</li>
				<li>1 prepared pie crust</li>
			</ul>

			<h2 class="subheading">Instructions</h2>
			<p class="body large-letter">
				Preheat your oven to 400°F (200°C). In a large skillet, heat olive oil over medium heat. 
				Add the chopped onion and sauté until translucent, then add minced garlic and cook until fragrant.
			</p>
			<p class="body">
				Gradually whisk in the flour to create a roux, cooking for about a minute. Slowly pour in the 
				vegetable broth and almond milk, whisking constantly to prevent lumps. Add the frozen vegetables, 
				potato chunks, and vegan chicken or tofu. Simmer until the sauce thickens and the vegetables are 
				tender. Season with salt to taste.
			</p>
			<p class="body">
				Pour the filling into your prepared pie crust, ensuring an even distribution. Bake in the preheated 
				oven for 45 minutes, or until the crust is golden brown and the filling is bubbling. Let cool for 
				10 minutes before serving.
			</p>
